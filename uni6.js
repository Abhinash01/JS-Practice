for (let i = 1; i <= 25; i++) {
    if (i >= 1 && i <= 10) {
      console.log("Low");
    } else if (i >= 11 && i <= 20) {
      console.log("Medium");
    } else if (i >= 21 && i <= 25) {
      console.log("High");
    }
  }

  
//   Range Identifier (1–25)

// Loop from 1 to 25.
// If number is between 1–10 → print "Low"
// 11–20 → print "Medium"
// 21–25 → print "High"